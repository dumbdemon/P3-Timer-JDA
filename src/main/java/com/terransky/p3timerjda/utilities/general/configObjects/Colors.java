package com.terransky.p3timerjda.utilities.general.configObjects;

@SuppressWarnings("unused")
public class Colors {

    private String main;
    private String sub;
    private String error;

    Colors() {
    }

    public String getMain() {
        return main;
    }

    public void setMain(String main) {
        this.main = main;
    }

    public String getSub() {
        return sub;
    }

    public void setSub(String sub) {
        this.sub = sub;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }
}
